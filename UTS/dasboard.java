    private void refreshTable() {
        model.setRowCount(0);
        for (Obat o : service.getAll()) {
            model.addRow(new Object[]{
                o.getKodeObat(),
                o.getNamaObat(),
                o.getIdKategori(),
                o.getKodeSatuan()
            });
        }
    }

