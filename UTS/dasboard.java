    private void btnnamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnamaActionPerformed
        // TODO add your handling code here:
        Obat o = new Obat(
                Kode_Obat.getText(),
                Nama_Obat.getText(),
                Id_Kategori.getText(),
                Kode_Satuan.getText()
        );

        service.tambahObat(o);
        refreshTable();
    }//GEN-LAST:event_btnnamaActionPerformed

    private void btnnama1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnama1ActionPerformed
        // TODO add your handling code here:
        String kode = kode_obat.getText();

        Obat obatBaru = new Obat(
                kode,
                nama.getText(),
                id_kategori.getText(),
                kode_satuan.getText()
        );

        boolean berhasil = service.updateObat(kode, obatBaru);

        if (berhasil) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Data obat berhasil diperbarui");
            refreshTable();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Kode obat tidak ditemukan!",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnnama1ActionPerformed

